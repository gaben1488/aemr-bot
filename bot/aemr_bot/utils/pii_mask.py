"""Маскирование ПДн для вывода оператору (admin-чат, XLSX-выгрузки).

Каноничное место для `mask_phone` — раньше существовало три копии
(`services/admin_events.py::_mask_phone`,
`handlers/admin_audience.py::_mask_phone`, и отсутствующая маска в
`services/stats.py`, где телефон уходил в XLSX без маски вовсе).
Дублирование опасно само по себе (риск разъехаться при будущей правке
формата), а немаскированный телефон в XLSX — прямая утечка ПДн 152-ФЗ
в файл, который скачивает оператор и может переслать/оставить на
диске без контроля доступа админ-чата.
"""
from __future__ import annotations


def mask_phone(phone: str | None) -> str:
    """Маскированный телефон: «+7***1234».

    152-ФЗ: операторы видят 4 последние цифры для идентификации
    жителя, но не полный номер (он попадает в backup MAX-серверов,
    скриншоты операторов и, для XLSX-выгрузок, на диск оператора).

    SECURITY_REVIEW_2026-05-28 §A7: при len(digits) < 4 возвращаем
    «—», а не raw `phone` — короткие/частично стёртые номера иначе
    утекали бы без маски. Лучше «нет данных», чем «выдали как есть».
    """
    if not phone:
        return "—"
    digits = "".join(ch for ch in phone if ch.isdigit())
    if len(digits) < 4:
        return "—"
    tail = digits[-4:]
    prefix = "+7" if digits[0] in {"7", "8"} and len(digits) >= 11 else "+"
    return f"{prefix}***{tail}"
